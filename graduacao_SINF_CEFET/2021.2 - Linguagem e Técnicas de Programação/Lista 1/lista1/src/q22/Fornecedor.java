package q22;

public class Fornecedor {
	private String nome;
	private String telefone;
	private String endereco;
	private String CNPJ;
	
	public Fornecedor(String nome, String telefone, String endereco, String cNPJ) {
		super();
		this.nome = nome;
		this.telefone = telefone;
		this.endereco = endereco;
		CNPJ = cNPJ;
	}
	
	
}
